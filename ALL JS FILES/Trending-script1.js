// for play function
let isPlaying38 = false;
const playMusic38 = () => {
    isPlaying38 = true

    music38.play();
    play38.classList.replace("fa-play", "fa-pause");
    img38.classList.add("anime38");
};

// for pause function
const pauseMusic38 = () => {
    isPlaying38 = false

    music38.pause();
    play38.classList.replace("fa-pause", "fa-play");
    img38.classList.remove("anime38");
};

