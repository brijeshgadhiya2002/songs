// changing music data
const loadSong38 = (songs38) => {
    title38.textContent = songs38.title38
    artist38.textContent = songs38.artist38
    music38.src = "music38/" + songs38.name38 + ".mp3"
    img38.src = "images/" + songs38.name38 + ".webp"
};

songIndex38 = 0;
// loadSong38(songs38[2]);
const nextSong38 = () => {
    songIndex38 = (songIndex38 + 1) % songs38.length;
    loadSong38(songs38[songIndex38]);
    playMusic38();
}

const prevSong38 = () => {
    songIndex38 = (songIndex38 - 1 + songs38.length) % songs38.length;
    loadSong38(songs38[songIndex38]);
    playMusic38();
}

// progress js work 
music38.addEventListener('timeupdate', (event) => {
    // console.log(event);
    const { currentTime, duration } = event.srcElement;
    let progress_time = (currentTime / duration) * 100;
    progress38.style.width = `${progress_time}%`;

    // music duration update 
    let min_duration = Math.floor(duration / 60);
    let sec_duration = Math.floor(duration % 60);

    let tot_duration = `${min_duration}:${sec_duration}`;
    if (duration) {
        total_duration38.textContent = `${tot_duration}`;
    }

    // current duration update 
    let min_currentTime = Math.floor(currentTime / 60);
    let sec_currentTime = Math.floor(currentTime % 60);
    if (sec_currentTime < 10) {
        sec_currentTime = `0${sec_currentTime}`;
    }
    let tot_currentTime = `${min_currentTime}:${sec_currentTime}`;
    current_time38.textContent = `${tot_currentTime}`;
});

// progress onclick functionality 
progress_div38.addEventListener('click', (event) => {
    const { duration } = music38;
    let move_progress = (event.offsetX / event.srcElement.clientWidth) * duration;
    music38.currentTime = move_progress;
});

