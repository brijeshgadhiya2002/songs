// for play function
let isPlaying2 = false;
const playMusic2 = () => {
    isPlaying2 = true

    music2.play();
    play2.classList.replace("fa-play", "fa-pause");
    img2.classList.add("anime2");
};

// for pause function
const pauseMusic2 = () => {
    isPlaying2 = false

    music2.pause();
    play2.classList.replace("fa-pause", "fa-play");
    img2.classList.remove("anime2");
};

play2.addEventListener("click", () => {
    isPlaying2 ? pauseMusic2() : playMusic2();
});

// changing music data
const loadSong2 = (songs2) => {
    title2.textContent = songs2.title2
    artist2.textContent = songs2.artist2
    music2.src = "music2/" + songs2.name2 + ".mp3"
    img2.src = "images/" + songs2.name2 + ".webp"
};
songIndex2 = 0;
// loadSong2(songs2[2]);
const nextSong2 = () => {
    songIndex2 = (songIndex2 + 1) % songs2.length;
    loadSong2(songs2[songIndex2]);
    playMusic2();
}
const prevSong2 = () => {
    songIndex2 = (songIndex2 - 1 + songs2.length) % songs2.length;
    loadSong2(songs2[songIndex2]);
    playMusic2();
}

// progress js work
music2.addEventListener('timeupdate', (event) => {
    // console.log(event);
    const { currentTime, duration } = event.srcElement;
    let progress_time = (currentTime / duration) * 100;
    progress2.style.width = `${progress_time}%`;

    // music duration update 
    let min_duration = Math.floor(duration / 60);
    let sec_duration = Math.floor(duration % 60);

    let tot_duration = `${min_duration}:${sec_duration}`;
    if (duration) {
        total_duration.textContent = `${tot_duration}`;
    }

    // current duration update 
    let min_currentTime = Math.floor(currentTime / 60);
    let sec_currentTime = Math.floor(currentTime % 60);
    if (sec_currentTime < 10) {
        sec_currentTime = `0${sec_currentTime}`;
    }
    let tot_currentTime = `${min_currentTime}:${sec_currentTime}`;
    current_time2.textContent = `${tot_currentTime}`;
});

// progress onclick functionality 
progress_div2.addEventListener('click', (event) => {
    const { duration } = music2;
    let move_progress = (event.offsetX / event.srcElement.clientWidth) * duration;
    music2.currentTime = move_progress;
});

// if music end next song Function 
music2.addEventListener('ended', nextSong2);

next2.addEventListener("click", nextSong2);
prev2.addEventListener("click", prevSong2);
