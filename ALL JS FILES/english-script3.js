// for play function
let isPlaying20 = false;
const playMusic20 = () => {
    isPlaying20 = true

    music20.play();
    play20.classList.replace("fa-play", "fa-pause");
    img20.classList.add("anime20");
};

// for pause function
const pauseMusic20 = () => {
    isPlaying20 = false

    music20.pause();
    play20.classList.replace("fa-pause", "fa-play");
    img20.classList.remove("anime20");
};

play20.addEventListener("click", () => {
    isPlaying20 ? pauseMusic20() : playMusic20();
});

// changing music data
const loadSong20 = (songs20) => {
    title20.textContent = songs20.title20
    artist20.textContent = songs20.artist20
    music20.src = "music20/" + songs20.name20 + ".mp3"
    img20.src = "images/" + songs20.name20 + ".webp"
};

songIndex20 = 0;
// loadSong20(songs20[2]);
const nextSong20 = () => {
    songIndex20 = (songIndex20 + 1) % songs20.length;
    loadSong20(songs20[songIndex20]);
    playMusic20();
}

const prevSong20 = () => {
    songIndex20 = (songIndex20 - 1 + songs20.length) % songs20.length;
    loadSong20(songs20[songIndex20]);
    playMusic20();
}

// progress js work 
music20.addEventListener('timeupdate', (event) => {
    // console.log(event);
    const { currentTime, duration } = event.srcElement;
    let progress_time = (currentTime / duration) * 100;
    progress20.style.width = `${progress_time}%`;

    // music duration update 
    let min_duration = Math.floor(duration / 60);
    let sec_duration = Math.floor(duration % 60);

    let tot_duration = `${min_duration}:${sec_duration}`;
    if (duration) {
        total_duration20.textContent = `${tot_duration}`;
    }

    // current duration update 
    let min_currentTime = Math.floor(currentTime / 60);
    let sec_currentTime = Math.floor(currentTime % 60);
    if (sec_currentTime < 10) {
        sec_currentTime = `0${sec_currentTime}`;
    }
    let tot_currentTime = `${min_currentTime}:${sec_currentTime}`;
    current_time20.textContent = `${tot_currentTime}`;
});

// progress onclick functionality 
progress_div20.addEventListener('click', (event) => {
    const { duration } = music20;
    let move_progress = (event.offsetX / event.srcElement.clientWidth) * duration;
    music20.currentTime = move_progress;
});

// if music end next song Function 
music20.addEventListener('ended', nextSong20);

next20.addEventListener("click", nextSong20);
prev20.addEventListener("click", prevSong20);