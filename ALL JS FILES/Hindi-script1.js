// changing music data
const loadSong33 = (songs33) => {
    title33.textContent = songs33.title33
    artist33.textContent = songs33.artist33
    music33.src = "music33/" + songs33.name33 + ".mp3"
    img33.src = "images/" + songs33.name33 + ".webp"
};

songIndex33 = 0;
// loadSong33(songs33[2]);
const nextSong33 = () => {
    songIndex33 = (songIndex33 + 1) % songs33.length;
    loadSong33(songs33[songIndex33]);
    playMusic33();
}

const prevSong33 = () => {
    songIndex33 = (songIndex33 - 1 + songs33.length) % songs33.length;
    loadSong33(songs33[songIndex33]);
    playMusic33();
}

// progress js work 
music33.addEventListener('timeupdate', (event) => {
    // console.log(event);
    const { currentTime, duration } = event.srcElement;
    let progress_time = (currentTime / duration) * 100;
    progress33.style.width = `${progress_time}%`;

    // music duration update 
    let min_duration = Math.floor(duration / 60);
    let sec_duration = Math.floor(duration % 60);

    let tot_duration = `${min_duration}:${sec_duration}`;
    if (duration) {
        total_duration33.textContent = `${tot_duration}`;
    }

    // current duration update 
    let min_currentTime = Math.floor(currentTime / 60);
    let sec_currentTime = Math.floor(currentTime % 60);
    if (sec_currentTime < 10) {
        sec_currentTime = `0${sec_currentTime}`;
    }
    let tot_currentTime = `${min_currentTime}:${sec_currentTime}`;
    current_time33.textContent = `${tot_currentTime}`;
});

// progress onclick functionality 
progress_div33.addEventListener('click', (event) => {
    const { duration } = music33;
    let move_progress = (event.offsetX / event.srcElement.clientWidth) * duration;
    music33.currentTime = move_progress;
});

// if music end next song Function 
music33.addEventListener('ended', nextSong33);

next33.addEventListener("click", nextSong33);
prev33.addEventListener("click", prevSong33);

