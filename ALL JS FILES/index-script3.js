// for play function
let isPlaying3 = false;
const playMusic3 = () => {
    isPlaying3 = true

    music3.play();
    play3.classList.replace("fa-play", "fa-pause");
    img3.classList.add("anime3");
};

// for pause function
const pauseMusic3 = () => {
    isPlaying3 = false

    music3.pause();
    play3.classList.replace("fa-pause", "fa-play");
    img3.classList.remove("anime3");
};

play3.addEventListener("click", () => {
    isPlaying3 ? pauseMusic3() : playMusic3();
});

// changing music data
const loadSong3 = (songs3) => {
    title3.textContent = songs3.title3
    artist3.textContent = songs3.artist3
    music3.src = "music3/" + songs3.name3 + ".mp3"
    img3.src = "images/" + songs3.name3 + ".webp"
};

songIndex3 = 0;
// loadSong3(songs3[2]);
const nextSong3 = () => {
    songIndex3 = (songIndex3 + 1) % songs3.length;
    loadSong3(songs3[songIndex3]);
    playMusic3();
}

const prevSong3 = () => {
    songIndex3 = (songIndex3 - 1 + songs3.length) % songs3.length;
    loadSong3(songs3[songIndex3]);
    playMusic3();
}

// progress js work 
music3.addEventListener('timeupdate', (event) => {
    // console.log(event);
    const { currentTime, duration } = event.srcElement;
    let progress_time = (currentTime / duration) * 100;
    progress3.style.width = `${progress_time}%`;

    // music duration update 
    let min_duration = Math.floor(duration / 60);
    let sec_duration = Math.floor(duration % 60);

    let tot_duration = `${min_duration}:${sec_duration}`;
    if (duration) {
        total_duration.textContent = `${tot_duration}`;
    }

    // current duration update 
    let min_currentTime = Math.floor(currentTime / 60);
    let sec_currentTime = Math.floor(currentTime % 60);
    if (sec_currentTime < 10) {
        sec_currentTime = `0${sec_currentTime}`;
    }
    let tot_currentTime = `${min_currentTime}:${sec_currentTime}`;
    current_time3.textContent = `${tot_currentTime}`;
});

// progress onclick functionality 
progress_div3.addEventListener('click', (event) => {
    const { duration } = music3;
    let move_progress = (event.offsetX / event.srcElement.clientWidth) * duration;
    music3.currentTime = move_progress;
});

// if music end next song Function 
music3.addEventListener('ended', nextSong3);

next3.addEventListener("click", nextSong3);
prev3.addEventListener("click", prevSong3);






