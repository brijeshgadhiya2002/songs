// changing music data
const loadSong18 = (songs18) => {
    title18.textContent = songs18.title18
    artist18.textContent = songs18.artist18
    music18.src = "music18/" + songs18.name18 + ".mp3"
    img18.src = "images/" + songs18.name18 + ".webp"
};

songIndex18 = 0;
// loadSong18(songs18[2]);
const nextSong18 = () => {
    songIndex18 = (songIndex18 + 1) % songs18.length;
    loadSong18(songs18[songIndex18]);
    playMusic18();
}

const prevSong18 = () => {
    songIndex18 = (songIndex18 - 1 + songs18.length) % songs18.length;
    loadSong18(songs18[songIndex18]);
    playMusic18();
}

// progress js work 
music18.addEventListener('timeupdate', (event) => {
    // console.log(event);
    const { currentTime, duration } = event.srcElement;
    let progress_time = (currentTime / duration) * 100;
    progress18.style.width = `${progress_time}%`;

    // music duration update 
    let min_duration = Math.floor(duration / 60);
    let sec_duration = Math.floor(duration % 60);

    let tot_duration = `${min_duration}:${sec_duration}`;
    if (duration) {
        total_duration18.textContent = `${tot_duration}`;
    }

    // current duration update 
    let min_currentTime = Math.floor(currentTime / 60);
    let sec_currentTime = Math.floor(currentTime % 60);
    if (sec_currentTime < 10) {
        sec_currentTime = `0${sec_currentTime}`;
    }
    let tot_currentTime = `${min_currentTime}:${sec_currentTime}`;
    current_time18.textContent = `${tot_currentTime}`;
});

// progress onclick functionality 
progress_div18.addEventListener('click', (event) => {
    const { duration } = music18;
    let move_progress = (event.offsetX / event.srcElement.clientWidth) * duration;
    music18.currentTime = move_progress;
});

