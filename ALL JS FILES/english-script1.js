let isPlaying18 = false;
const playMusic18 = () => {
    isPlaying18 = true

    music18.play();
    play18.classList.replace("fa-play", "fa-pause");
    img18.classList.add("anime18");
};

// for pause function
const pauseMusic18 = () => {
    isPlaying18 = false

    music18.pause();
    play18.classList.replace("fa-pause", "fa-play");
    img18.classList.remove("anime18");
};

